/**
 * Inject MiniMax Token Plan key into secrets.local.ts from the user's env files.
 * Never prints the key. Output is gitignored.
 */
import fs from 'node:fs';
import path from 'node:path';
import os from 'node:os';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outFile = path.resolve(__dirname, '../src/personal/secrets.local.ts');

function parseEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return {};
  const out = {};
  for (const line of fs.readFileSync(filePath, 'utf8').split(/\r?\n/)) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const i = t.indexOf('=');
    if (i <= 0) continue;
    const k = t.slice(0, i).trim();
    let v = t.slice(i + 1).trim();
    if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) {
      v = v.slice(1, -1);
    }
    out[k] = v;
  }
  return out;
}

const candidates = [
  path.join(os.homedir(), '.config/ai-providers/env.local'),
  path.join(os.homedir(), '.config/ai-providers/.env'),
  path.resolve(__dirname, '../../../.env.local'),
  path.resolve(__dirname, '../../.env.local'),
];

let env = {};
for (const p of candidates) {
  env = { ...env, ...parseEnvFile(p) };
}

// Prefer explicit process env
for (const k of ['MINIMAX_API_KEY', 'MINIMAX_TOKEN_PLAN_KEY']) {
  if (process.env[k]) env[k] = process.env[k];
}

const key = (env.MINIMAX_API_KEY || env.MINIMAX_TOKEN_PLAN_KEY || '').trim();
if (!key) {
  console.error(
    '[inject-personal-secrets] No MINIMAX_API_KEY / MINIMAX_TOKEN_PLAN_KEY found in env files or process.env.',
  );
  console.error('Checked:', candidates.join(', '));
  process.exit(1);
}

const content = `/**
 * AUTO-GENERATED by inject-personal-secrets.mjs — DO NOT COMMIT
 * Generated: ${new Date().toISOString()}
 */
export const PERSONAL_MINIMAX_API_KEY = ${JSON.stringify(key)};
`;

fs.writeFileSync(outFile, content, { mode: 0o600 });
console.log(`[inject-personal-secrets] Wrote secrets.local.ts (key length=${key.length}, prefix=${key.slice(0, 6)}...)`);
